using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Ejercicio01___Imprimir_del_10_al_1
{
    internal class Ejercicio01
    {
        static void Main(string[] args)
        {
            // 1. Imprima los números del 10 al 1.

            for (int i = 10; i > 0; i--)
            {
                Console.Write(i + " ");
            }
            Console.WriteLine();
        }
    }
}
